export function findMatchingScenario(url) {
  const trimmed = url.trim();
  for (const scenario of scenarios) {
    if (scenario.matchUrl(trimmed)) {
      return scenario;
    }
  }
  return null;
}

export function getEmulatedResponse(url) {
  const scenario = findMatchingScenario(url);
  if (!scenario) {
    return {
      found: false,
      status: 404,
      error: 'Not Found',
      message: 'Ответ сервера: 404 Not Found (эмулировано). Указанный URL не соответствует ни одному известному вектору SSRF-атаки. Попробуйте выбрать один из сценариев в выпадающем списке выше.',
      hint: 'Подсказка: выберите сценарий из списка, и поле URL заполнится автоматически. Или попробуйте вручную: http://169.254.169.254/latest/meta-data/, http://localhost/admin, file:///etc/passwd',
    };
  }
  const response = scenario.emulatedResponse(url);
  return {
    found: true,
    scenarioName: scenario.name,
    scenarioId: scenario.id,
    severity: scenario.severity,
    category: scenario.category,
    data: response,
    responseAnnotation: scenario.responseAnnotation,
    preExplanation: scenario.preExplanation,
    explanation: scenario.explanation,
    realWorldExample: scenario.realWorldExample,
    stepByStep: scenario.stepByStep,
  };
}